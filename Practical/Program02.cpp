#include<stdio.h>

main()
{
	char ch;
	printf("Enter First char of your name :");
	scanf("%c",&ch);
	printf("First char of your name is : %c\n",ch);
	
	int age;
	printf("Enter Your age :");
	scanf("%d",&age);
	printf("Age is : %d\n",age);
	
	float pi;
	printf("Enter value of pi :");
	scanf("%f",&pi);
	printf("Value of PI is : %f\n",pi);
}
